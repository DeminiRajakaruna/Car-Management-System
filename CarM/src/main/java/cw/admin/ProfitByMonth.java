package cw.admin;

public class ProfitByMonth {
    public String JAN;
    public String FEB ;
    public String MAR ;
    public String APR ;
    public String MAY;
    public String JUN ;
    public String JULY ;
    public String AUG  ;
    public String SEP ;
    public String OCT ;
    public String NOV ;
    public String DEC ;
    public ProfitByMonth(String JAN, String FEB, String MAR, String APR, String MAY, String JUN, String JULY, String AUG, String SEP, String OCT, String NOV, String DEC) {
        this.JAN = JAN;
        this.FEB = FEB;
        this.MAR = MAR;
        this.APR = APR;
        this.MAY = MAY;
        this.JUN = JUN;
        this.JULY = JULY;
        this.AUG = AUG;
        this.SEP = SEP;
        this.OCT = OCT;
        this.NOV = NOV;
        this.DEC = DEC;
    }

    public String getJAN() {
        return JAN;
    }

    public void setJAN(String JAN) {
        this.JAN = JAN;
    }

    public String getFEB() {
        return FEB;
    }

    public void setFEB(String FEB) {
        this.FEB = FEB;
    }

    public String getMAR() {
        return MAR;
    }

    public void setMAR(String MAR) {
        this.MAR = MAR;
    }

    public String getAPR() {
        return APR;
    }

    public void setAPR(String APR) {
        this.APR = APR;
    }

    public String getMAY() {
        return MAY;
    }

    public void setMAY(String MAY) {
        this.MAY = MAY;
    }

    public String getJUN() {
        return JUN;
    }

    public void setJUN(String JUN) {
        this.JUN = JUN;
    }

    public String getJULY() {
        return JULY;
    }

    public void setJULY(String JULY) {
        this.JULY = JULY;
    }

    public String getAUG() {
        return AUG;
    }

    public void setAUG(String AUG) {
        this.AUG = AUG;
    }

    public String getSEP() {
        return SEP;
    }

    public void setSEP(String SEP) {
        this.SEP = SEP;
    }

    public String getOCT() {
        return OCT;
    }

    public void setOCT(String OCT) {
        this.OCT = OCT;
    }

    public String getNOV() {
        return NOV;
    }

    public void setNOV(String NOV) {
        this.NOV = NOV;
    }

    public String getDEC() {
        return DEC;
    }

    public void setDEC(String DEC) {
        this.DEC = DEC;
    }
}
